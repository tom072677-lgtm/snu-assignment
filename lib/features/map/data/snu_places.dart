/// 쿼리와 매칭되는 SNU 장소 반환 (검색어 포함 관계 기반)
List<PlaceResult> searchSnuLocal(String query) {
  // "서울대", "서울대학교" 접두사 제거 후 핵심어만 추출
  final core = query
      .trim()
      .replaceAll('서울대학교', '')
      .replaceAll('서울대', '')
      .trim()
      .toLowerCase();
  if (core.isEmpty) return [];

  // 중복 표시명 제거 (같은 fullName의 첫 번째만)
  final seen = <String>{};
  final results = <PlaceResult>[];
  for (final p in _places) {
    final key = p.$1.toLowerCase();
    if (key.contains(core) || core.contains(key)) {
      if (seen.add(p.$2)) {
        results.add(PlaceResult(
          name: p.$2,
          address: p.$3,
          lat: p.$4,
          lng: p.$5,
          category: '대학교',
        ));
      }
    }
  }
  return results;
}
